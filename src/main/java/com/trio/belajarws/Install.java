package com.trio.belajarws;

import java.util.Date;

public class Install {
	
	private Date dinstall;
	private String vidversion;
	private String vdescription;
	private String vidpatch;
	
	public Date getDinstall() {
		return dinstall;
	}
	public void setDinstall(Date dinstall) {
		this.dinstall = dinstall;
	}
	public String getVidversion() {
		return vidversion;
	}
	public void setVidversion(String vidversion) {
		this.vidversion = vidversion;
	}
	public String getVdescription() {
		return vdescription;
	}
	public void setVdescription(String vdescription) {
		this.vdescription = vdescription;
	}
	public String getVidpatch() {
		return vidpatch;
	}
	public void setVidpatch(String vidpatch) {
		this.vidpatch = vidpatch;
	}
	

}
